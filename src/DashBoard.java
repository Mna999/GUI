    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
       String line;
    String[] data;
    File f= new File("Employees.txt");
    DefaultTableModel model= (DefaultTableModel) table.getModel();
int j=0;
        try {
            Scanner s = new Scanner(f);
            while(s.hasNextLine()){
            line=s.nextLine();
            data=line.split("/");
            
            if (j >= model.getRowCount()) {
                model.addRow(new Object[data.length]);
            }
            for(int i=0;i<data.length;i++){
            if(!(data[i].equals(model.getValueAt(j,i)))){
            model.setValueAt(data[i],j,i);
            }
            }
            j++;
        }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"File doesnt exist","Error",JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
   
    DefaultTableModel model = (DefaultTableModel)table.getModel();
        boolean match=true;
    if(!stack.isEmpty()){
       Action current = stack.pop();
    if(current.getName().equals("add")){
    
    for(int i=0;i<model.getRowCount();i++){
        match=true;
    for(int j=0;j<model.getColumnCount();j++){
    Object [] data= current.getData();
    if(!model.getValueAt(i,j).equals(data[j].toString())){
    match=false;
    break;
    }
    }    
   if(match){
    model.removeRow(i);
    break;
    }
    }
    }
    else if (current.getName().equals("delete")) {
            boolean rowExists = false;
            Object[] rowdata = current.getData();
            for (int i = 0; i < model.getRowCount(); i++) {
                match = true;
                for (int j = 0; j < model.getColumnCount(); j++) {
                    if (!rowdata[j].equals(model.getValueAt(i, j).toString())) {
                        match = false;
                        break;
                    }
                }
                if (match) {
                    rowExists = true;
                    break;
                }
            }
            if (!rowExists) {
                model.addRow(rowdata);
            }
        }
    }
    
    
    }//GEN-LAST:event_jButton5ActionPerformed
    
