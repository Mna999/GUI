    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
  
   DefaultTableModel model= (DefaultTableModel) table.getModel();
   if(model.getRowCount()<=0){
   JOptionPane.showMessageDialog(this,"Table is empty","Error",JOptionPane.ERROR_MESSAGE);
   }
   else{
   File f= new File("Employees.txt");
   FileWriter fw;
   BufferedWriter bw;
        try {
            fw = new FileWriter(f);
            bw=new BufferedWriter(fw);
                for (int row = 0; row < model.getRowCount(); row++) {
          
            bw.write(model.getValueAt(row, 0).toString());
            for (int col = 1; col < model.getColumnCount(); col++) {
               bw.write("/"+model.getValueAt(row, col).toString());
            }
            
            
            bw.newLine();
        }
        bw.close();
        fw.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this,"An error has occured","Error",JOptionPane.ERROR_MESSAGE);
        }
        
        JOptionPane.showMessageDialog(this,"Saved Sucessfully","Information",JOptionPane.INFORMATION_MESSAGE);
 
   }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void loadFile(){
    String line;
    String[] data;
    File f= new File("Employees.txt");
    DefaultTableModel model= (DefaultTableModel) table.getModel();
        try {
            Scanner s = new Scanner(f);
            while(s.hasNextLine()){
            line=s.nextLine();
            data=line.split("/");
            model.addRow(data);
            }
        } catch (FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this,"File doesnt exist","Error",JOptionPane.ERROR_MESSAGE);
        }
    }
    
    
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      String name = nameField.getText();
        String email = emailField.getText();
        String phone = phoneField.getText();
        String address = addressField.getText();

        if (name.isBlank() || email.isBlank() || phone.isBlank() || address.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter all fields", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.addRow(new Object[]{name, email, phone, address});
            stack.push(new Action("add", new Object[]{name, email, phone, address}));
            JOptionPane.showMessageDialog(this, "Employee added successfully", "Information", JOptionPane.INFORMATION_MESSAGE);
            nameField.setText("");
            emailField.setText("");
            phoneField.setText("");
            addressField.setText("");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    
